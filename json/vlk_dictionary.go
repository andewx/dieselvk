//Gets the string integer value and allows for concatenation of integers with OR | characters
func (dictionary *Dictionary) Get(s string) int {
	str := strings.Trim(s, " ")
	all := strings.Split(str, "|")
	value := 0
	for _, n := range all {
		value = value | dictionary.ints[n]
	}
	return value
}
